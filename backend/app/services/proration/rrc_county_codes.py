def lookup_county(county_name: str) -> tuple[str, str, str] | None:
    """Look up RRC county code and district for a county name.

    Args:
        county_name: County name (case-insensitive, "County" suffix stripped)

    Returns:
        (county_code, district, canonical_name) or None if not found
    """
    name = county_name.strip().upper().replace(" COUNTY", "")
    entry = TEXAS_COUNTY_CODES.get(name)
    if entry:
        return entry[0], entry[1], name
    return None
